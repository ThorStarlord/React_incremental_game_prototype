// Thunk Actions

/**
 * Hire a minion if the player has enough resources
 * @param {Object} minionData - The minion to hire
 * @returns {Function} Thunk function
 */
export const hireMinionIfAffordable = (minionData) => (dispatch, getState) => {
  const { resources } = getState();
  
  if (resources.gold >= minionData.cost) {
    dispatch(hireMinion(minionData));
    dispatch({
      type: 'resources/spend',
      payload: { gold: minionData.cost }
    });
    return true;
  }
  
  return false;
};

