/**
 * Collect resources from all minions and add to player's inventory
 * This is typically called on a timer or when the player clicks a "collect" button
 * @returns {Function} Thunk function
 */
export const harvestMinionResources = () => (dispatch, getState) => {
  const { minions } = getState();
  let totalResources = { gold: 0, wood: 0, stone: 0, food: 0, iron: 0 };
  
  // Calculate resources generated by all assigned minions
  minions.forEach(minion => {
    if (minion.assigned && minion.productionRate) {
      Object.keys(minion.productionRate).forEach(resource => {
        if (totalResources[resource] !== undefined) {
          totalResources[resource] += minion.productionRate[resource];
        }
      });
    }
  });
  
  // Add resources to inventory
  if (Object.values(totalResources).some(val => val > 0)) {
    dispatch({
      type: 'resources/add',
      payload: totalResources
    });
    dispatch(collectMinionResources());
  }
  
  return totalResources;
};
