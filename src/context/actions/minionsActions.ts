// Thunk Actions

/**
 * Hire a minion if the player has enough resources
 * @param {MinionData} minionData - The minion to hire
 * @returns {ThunkAction<boolean>} Thunk function
 */
export const hireMinionIfAffordable = (minionData: MinionData): ThunkAction<boolean> => (dispatch, getState) => {
  const { resources } = getState();
  
  if (resources.gold >= minionData.cost) {
    dispatch(hireMinion(minionData));
    dispatch({
      type: 'resources/spend',
      payload: { gold: minionData.cost }
    });
    return true;
  }
  
  return false;
};

