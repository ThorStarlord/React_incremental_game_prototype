/**
 * Collect resources from all minions and add to player's inventory
 * This is typically called on a timer or when the player clicks a "collect" button
 * @returns {ThunkAction<Record<string, number>>} Thunk function
 */
export const harvestMinionResources = (): ThunkAction<Record<string, number>> => (dispatch, getState) => {
  interface ResourceProductions {
    gold?: number;
    wood?: number;
    stone?: number;
    food?: number;
    iron?: number;
    [key: string]: number | undefined;
  }

  interface Minion {
    id: string;
    assigned: boolean;
    productionRate?: ResourceProductions;
    [key: string]: any;
  }

  const { minions } = getState();
  let totalResources: ResourceProductions = { gold: 0, wood: 0, stone: 0, food: 0, iron: 0 };
  
  // Calculate resources generated by all assigned minions
  if (Array.isArray(minions)) {
    minions.forEach((minion: Minion) => {
      if (minion.assigned && minion.productionRate) {
        Object.keys(minion.productionRate).forEach(resource => {
          if (totalResources[resource] !== undefined && minion.productionRate?.[resource] !== undefined) {
            totalResources[resource] = (totalResources[resource] || 0) + (minion.productionRate[resource] || 0);
          }
        });
      }
    });
  }
  
  // Add resources to inventory
  if (Object.values(totalResources).some(val => (val || 0) > 0)) {
    dispatch({
      type: 'resources/add',
      payload: totalResources
    });
    dispatch(collectMinionResources());
  }
  
  return totalResources as Record<string, number>;
};
